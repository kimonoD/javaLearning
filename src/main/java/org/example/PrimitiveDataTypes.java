package org.example;

public class PrimitiveDataTypes {

    public static void playWithBooleans() {
        boolean hasCar;
        boolean hasGarage;
        hasCar = true;
        hasGarage = false;
        System.out.println("I have car " + hasCar);
        System.out.println("I have garage " + hasGarage);
    }

    public static boolean iHaveFunctionalChair(boolean hasLegs, boolean hasSeat){
        return hasLegs && hasSeat;
    }






}
